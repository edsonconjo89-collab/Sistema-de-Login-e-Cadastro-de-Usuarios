    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        if(verificarCampos()){
            String nome = txtNomeUsuario.getText();
            String senha = new String(pwfSenha.getPassword());
            TipoConta tipo = null;
            
            if(radioNormal.isSelected()){
                tipo = TipoConta.NORMAL;
            }
            
            if(radioAdministrador.isSelected()){
                tipo = TipoConta.ADMINISTRADOR;
            }
            
            ServicoUsuario servicoUsuario = AppContext.servicoUsuario;
            
            try{
                servicoUsuario.cadastrar(new Usuario(nome,senha,tipo));
                limparCampos();
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!","Sucesso",JOptionPane.INFORMATION_MESSAGE);
            }catch(IllegalArgumentException e){
                JOptionPane.showMessageDialog(null, e.getMessage(), "Erro ao cadastrar", JOptionPane.ERROR_MESSAGE);
                limparCampos();
            }
        }
    }//GEN-LAST:event_btCadastrarActionPerformed

