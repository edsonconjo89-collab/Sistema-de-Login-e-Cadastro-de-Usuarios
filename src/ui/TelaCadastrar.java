    private boolean verificarCampos(){
        if(txtNomeUsuario.getText().trim().isEmpty() || pwfSenha.getPassword().length == 0 || pwfSenhaConfirmar.getPassword().length == 0
                || (!radioNormal.isSelected() && !radioAdministrador.isSelected())){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
            return false;
        }else{
            if(!Arrays.equals(pwfSenha.getPassword(), pwfSenhaConfirmar.getPassword())){
                JOptionPane.showMessageDialog(null, "As senhas não coincidem!","Não foi possivel cadastrar",JOptionPane.WARNING_MESSAGE);
                pwfSenha.setText(null);
                pwfSenhaConfirmar.setText(null);
                return false;
            }
        }
        
        return true;
    }
    
    private void limparCampos(){
        txtNomeUsuario.setText(null);
        pwfSenha.setText(null);
        pwfSenhaConfirmar.setText(null);
    }

